SYSTEM_PROMPT='You are doing LLM assist for a CREXX GraphRAG scope gate.
CREXX already did deterministic matching. CREXX is the final judge.
Do not extract the complete graph.

Answer these assist fields:
- relation_cue_assist: likely relation families in the chunk, or none.
- alias_cue_assist: likely alias/origin/old-spelling evidence, or none.
- bridge_assist: low, average, or high chance this chunk connects useful graph clusters.
- complexity_assist: low, medium, or high extraction complexity.
- cheapest_next: skip, deterministic, small_model, medium_model, or gemma.
- why: one short reason.

Be practical. It is fine to disagree with the deterministic hints.'

chat() {
  local label="$1"
  local base_url="$2"
  local model="$3"
  local max_tokens="$4"
  local timeout="$5"
  local user_prompt="$6"

  local body
  body="$(jq -n \
    --arg model "${model}" \
    --arg system "${SYSTEM_PROMPT}" \
    --arg user "${user_prompt}" \
    --argjson max_tokens "${max_tokens}" \
    '{
      model: $model,
      messages: [
        {role: "system", content: $system},
        {role: "user", content: $user}
      ],
      temperature: 0.1,
      max_tokens: $max_tokens,
      chat_template_kwargs: {enable_thinking: false},
      enable_thinking: false
    }')"

  echo
  echo "### ${label}"
  local raw
  if ! raw="$(curl -fsS --max-time "${timeout}" -w $'\n__time_total__:%{time_total}\n' \
    "${base_url}/chat/completions" \
    -H "Content-Type: application/json" \
    -H "Authorization: Bearer no-key" \
    -d "${body}" 2>&1)"; then
    echo
    echo '```text'
    echo "request failed or timed out after ${timeout}s"
    echo "${raw}"
    echo '```'
    return
  fi

  local json time content reasoning
  json="$(printf '%s' "${raw}" | sed '/^__time_total__:/d')"
  time="$(printf '%s' "${raw}" | sed -n 's/^__time_total__://p')"
  content="$(printf '%s' "${json}" | jq -r '.choices[0].message.content // ""')"
  reasoning="$(printf '%s' "${json}" | jq -r '.choices[0].message.reasoning_content // ""')"

  echo
  echo "time: ${time}s"
  echo
  echo '```text'
  if [[ -n "${content}" ]]; then
    printf '%s\n' "${content}"
  elif [[ -n "${reasoning}" ]]; then
    printf '%s\n' "${reasoning}"
  else
    printf '%s\n' "${json}"
  fi
  echo '```'
}

run_case() {
  local case_id="$1"
  local deterministic_scope="$2"
  local chunk="$3"

  local user_prompt
  user_prompt="$(cat <<EOF
Case: ${case_id}

Deterministic scope hints:
${deterministic_scope}

Source chunk:
${chunk}
EOF
)"

  echo
  echo "## ${case_id}"
  echo
  echo "### Deterministic Scope Hints"
  echo
  echo '```text'
  echo "${deterministic_scope}"
  echo '```'
  echo
  echo "### Source Chunk"
  echo
  echo '```text'
  echo "${chunk}"
  echo '```'

  chat "${QWEN3_LABEL}" "${QWEN3_BASE}" "${QWEN3_MODEL}" 300 20 "${user_prompt}"
  chat "${QWEN25_LABEL}" "${QWEN25_BASE}" "${QWEN25_MODEL}" 380 30 "${user_prompt}"
  chat "${GEMMA_LABEL}" "${GEMMA_BASE}" "${GEMMA_MODEL}" 480 90 "${user_prompt}"
}

