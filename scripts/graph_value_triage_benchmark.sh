#!/usr/bin/env bash
set -euo pipefail

ROOT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
CLI="${ROOT_DIR}/cmake-build-debug/crexx-rag"

QWEN3_BASE="${QWEN3_BASE:-http://127.0.0.1:8082/v1}"
QWEN3_MODEL="${QWEN3_MODEL:-ggml-org/Qwen3-0.6B-GGUF:Q4_0}"
QWEN3_LABEL="${QWEN3_LABEL:-Qwen3 0.6B fast}"
QWEN25_BASE="${QWEN25_BASE:-http://127.0.0.1:8083/v1}"
QWEN25_MODEL="${QWEN25_MODEL:-Qwen/Qwen2.5-1.5B-Instruct-GGUF:Q4_K_M}"
QWEN25_LABEL="${QWEN25_LABEL:-Qwen2.5 1.5B}"
GEMMA_BASE="${GEMMA_BASE:-http://127.0.0.1:8080/v1}"
GEMMA_MODEL="${GEMMA_MODEL:-ggml-org/gemma-4-E4B-it-GGUF:Q4_K_M}"
GEMMA_LABEL="${GEMMA_LABEL:-Gemma E4B}"

SYSTEM_PROMPT='You are advising a CREXX GraphRAG triage profile.
CREXX is the final judge. Do not extract a complete graph.

Given a fake known concept pool and one source chunk, answer these questions:
1. Is this chunk low, medium, or high value for typed graph construction?
2. What graph value would it add: typed relationship, alias/old spelling, bridge between clusters, or probably none?
3. What is the cheapest next step: skip, deterministic, small_model, medium_model, or gemma?
4. Name a few candidate typed jumps to look for, if any.
5. Say briefly what keyword/vector search might miss, if anything.

Be concise. Output format is flexible.'

chat() {
  local label="$1"
  local base_url="$2"
  local model="$3"
  local max_tokens="$4"
  local timeout="$5"
  local user_prompt="$6"

  local body
  body="$(jq -n \
    --arg model "${model}" \
    --arg system "${SYSTEM_PROMPT}" \
    --arg user "${user_prompt}" \
    --argjson max_tokens "${max_tokens}" \
    '{
      model: $model,
      messages: [
        {role: "system", content: $system},
        {role: "user", content: $user}
      ],
      temperature: 0.1,
      max_tokens: $max_tokens,
      chat_template_kwargs: {enable_thinking: false},
      enable_thinking: false
    }')"

  echo
  echo "### ${label}"

  local raw
  if ! raw="$(curl -fsS --max-time "${timeout}" -w $'\n__time_total__:%{time_total}\n' \
    "${base_url}/chat/completions" \
    -H "Content-Type: application/json" \
    -H "Authorization: Bearer no-key" \
    -d "${body}" 2>&1)"; then
    echo
    echo '```text'
    echo "request failed or timed out after ${timeout}s"
    echo "${raw}"
    echo '```'
    return
  fi

  local json time content reasoning
  json="$(printf '%s' "${raw}" | sed '/^__time_total__:/d')"
  time="$(printf '%s' "${raw}" | sed -n 's/^__time_total__://p')"
  content="$(printf '%s' "${json}" | jq -r '.choices[0].message.content // ""')"
  reasoning="$(printf '%s' "${json}" | jq -r '.choices[0].message.reasoning_content // ""')"

  echo
  echo "time: ${time}s"
  echo
  echo '```text'
  if [[ -n "${content}" ]]; then
    printf '%s\n' "${content}"
  elif [[ -n "${reasoning}" ]]; then
    printf '%s\n' "${reasoning}"
  else
    printf '%s\n' "${json}"
  fi
  echo '```'
}

run_case() {
  local case_id="$1"
  local expected="$2"
  local known_pool="$3"
  local chunk="$4"

  local user_prompt
  user_prompt="$(cat <<EOF
Case: ${case_id}
Expected reviewer intuition: ${expected}

Fake known concept pool:
${known_pool}

Source chunk:
${chunk}
EOF
)"

  echo
  echo "## ${case_id}"
  echo
  echo "Expected reviewer intuition: ${expected}"
  echo
  echo "### Fake Known Pool"
  echo
  echo '```text'
  echo "${known_pool}"
  echo '```'
  echo
  echo "### Source Chunk"
  echo
  echo '```text'
  echo "${chunk}"
  echo '```'

  chat "${QWEN3_LABEL}" "${QWEN3_BASE}" "${QWEN3_MODEL}" 320 20 "${user_prompt}"
  chat "${QWEN25_LABEL}" "${QWEN25_BASE}" "${QWEN25_MODEL}" 420 30 "${user_prompt}"
  chat "${GEMMA_LABEL}" "${GEMMA_BASE}" "${GEMMA_MODEL}" 520 90 "${user_prompt}"
}

